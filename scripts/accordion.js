$("#EVENTquestion1").click(function() {
    if ($("#EVENTanswer1").hasClass("hidden")) {
        $("#EVENTanswer1").removeClass("hidden");


        $("#EVENTquestion1tag > .arrow-up").removeClass("hidden");

        $("#EVENTquestion1tag > .arrow-down").addClass("hidden");


    } else {
        $("#EVENTanswer1").addClass("hidden");


        $("#EVENTquestion1tag > .arrow-up").addClass("hidden");
        $("#EVENTquestion1tag > .arrow-down").removeClass("hidden");

    }

});


